    public void refreshTable(){
        tm = new DefaultTableModel(
        null,
        new Object[]{"Id", "Judul", "Catatan"}
        );
        tblNotes.setModel(tm);
        tm.getDataVector().removeAllElements();
        
        try{
            PreparedStatement s = conn.prepareStatement("select * from notes");
            ResultSet r = s.executeQuery();
            
            while(r.next()){
                Object[] data = {
                    r.getString(1),
                    r.getString(2),
                    r.getString(3)
                };
                tm.addRow(data);
            }
        }catch(Exception e){
            System.out.println("Gagal terhubung dengan Database");
        }
    }

