    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String judul = txtJudul.getText();
        String catatan = txtCatatan.getText();
        
        try{
            ps = conn.prepareStatement("insert into notes(judul, catatan) values(?,?)");
            ps.setString(1, judul);
            ps.setString(2, catatan);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Catatan berhasil ditambahkan");
            refreshTable();
            
            txtJudul.setText("");
            txtCatatan.setText("");
           
        }catch(Exception e){
            Logger.getLogger(FormAdmin.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        DefaultTableModel Df = (DefaultTableModel)tblNotes.getModel();
        int selectedIndex = tblNotes.getSelectedRow();
        
        try{
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            String judul = txtJudul.getText();
            String catatan = txtCatatan.getText();
            
            ps = conn.prepareStatement("update notes set judul=?, catatan=? where id=?");
            ps.setString(1, judul);
            ps.setString(2, catatan);
            ps.setInt(3, id);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Catatan berhasil diedit");
            refreshTable();
            
            txtJudul.setText("");
            txtCatatan.setText("");
            
        }catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel Df = (DefaultTableModel)tblNotes.getModel();
        int selectedIndex = tblNotes.getSelectedRow();
        
        try{
            int id  = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            int dialogResult =  JOptionPane.showConfirmDialog(this, "Apakah anda yakin akan menghapus catatan ini?", "Warning", JOptionPane.YES_NO_OPTION);
            
            if(dialogResult == JOptionPane.YES_OPTION){
                ps = conn.prepareStatement("delete from notes where id=?");
                ps.setInt(1, id);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Catatan berhasil dihapus");
                refreshTable();
                
                txtJudul.setText("");
                txtCatatan.setText("");
                
            }
        }catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

