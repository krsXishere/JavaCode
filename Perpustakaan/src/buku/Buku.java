    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        if(txtKode.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Text ID Kosong","Informasi",1);
            txtKode.requestFocus();
        }else{
        
            DefaultTableModel tabelkol = new DefaultTableModel();
            tabelkol.addColumn("Kode Buku");
            tabelkol.addColumn("Judul Buku");
            tabelkol.addColumn("Pengarang");
            tabelkol.addColumn("Penerbit");
            tabelkol.addColumn("Stok");

            try {
                String sql = "Select * from koleksi where Kode like '%" +txtKode.getText()+"%'" +"or Judul like '%" + txtKode.getText() + "%'"; 
                Statement stat = conn.createStatement();
                rs = stat.executeQuery(sql);

                   txtKode.setText("");
                   txtJudul.setText("");
                   txtPengarang.setText("");
                   txtPenerbit.setText("");
                   txtStok.setText("");
                   txtKode.requestFocus();

                   btnHapus.setEnabled(true);
                   btnEdit.setEnabled(true);

                while (rs.next()) {
                    tabelkol.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
                }
               tblData.setModel(tabelkol);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan "+e);
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

