    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if((txtKode.getText().equals(""))||(txtJudul.getText().equals(""))||(txtPengarang.getText().equals(""))||(txtPenerbit.getText().equals(""))||(txtStok.getText().equals(""))){
            JOptionPane.showMessageDialog(btnSimpan, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
        }else{
            String sqlkode = "Insert into koleksi(Kode,Judul,Pengarang,Penerbit,Stock) values ('"+this.txtKode.getText()+"',"+ "'"+this.txtJudul.getText()+"',"+ "'"+this.txtPengarang.getText()+"',"+"'"+this.txtPenerbit.getText()+"',"+"'"+this.txtStok.getText()+"')";
            String sql = "Insert into stok(Kode,Jumlah) values ('"+this.txtKode.getText()+"',"+"'"+this.txtStok.getText()+"')";

                try { //jalankan query tersebut
                   ps = conn.prepareStatement(sqlkode);
                   ps.executeUpdate();
                   ps.close();

                   ps2 = conn.prepareStatement(sql);
                   ps2.executeUpdate();
                   ps2.close();
                   
                   txtKode.setText("");
                   txtJudul.setText("");
                   txtPengarang.setText("");
                   txtPenerbit.setText("");
                   txtStok.setText("");
                   btnEdit.setEnabled(true);
                   btnHapus.setEnabled(true);
                   txtKode.requestFocus();
                   refreshTable();

                   JOptionPane.showMessageDialog(this, "Simpan Berhasil","Informasi",1);
                }catch(SQLException ex) {
                
                   JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());

                   btnEdit.setEnabled(true);
                   btnHapus.setEnabled(true);
                   txtKode.requestFocus();
                   refreshTable();

                }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if((txtKode.getText().equals(""))||(txtJudul.getText().equals(""))||(txtPengarang.getText().equals(""))||(txtPenerbit.getText().equals(""))||(txtStok.getText().equals(""))){
            JOptionPane.showMessageDialog(btnSimpan, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
        }else{            
            try {
                String sqlkode = "Update koleksi SET Judul='"+this.txtJudul.getText()+"'," + "Pengarang='"+this.txtPengarang.getText()+"',"+"Penerbit='"+this.txtPenerbit.getText()+"',"+"Stock='"+this.txtStok.getText()+"'"+"Where Kode='"+this.txtKode.getText()+"'";
                String sql = "Update stok SET Jumlah='"+this.txtStok.getText()+"'"+"Where Kode='"+this.txtKode.getText()+"'";
                ps = conn.prepareStatement(sqlkode);
                ps.executeUpdate();
                ps.close();
                ps2 = conn.prepareStatement(sql);
                ps2.executeUpdate();
                ps2.close();
                JOptionPane.showMessageDialog(this, "Edit Berhasil","Informasi",1);
                txtKode.setText("");
                txtJudul.setText("");
                txtPengarang.setText("");
                txtPenerbit.setText("");
                txtStok.setText("");
                txtKode.requestFocus();

                refreshTable();
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

