    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        if(txtId.getText().equals("")){
              JOptionPane.showMessageDialog(this, "Text ID Kosong","Informasi",1);
              txtId.requestFocus();
        }else{
            DefaultTableModel tabelanggota = new DefaultTableModel();
            tabelanggota.addColumn("Id");
            tabelanggota.addColumn("Nama");
            tabelanggota.addColumn("Alamat");
            tabelanggota.addColumn("Tanggal Lahir");
            tabelanggota.addColumn("Jenis Kelamin");
            tabelanggota.addColumn("Pekerjaan");
            
            try {
                String sql = "Select * from anggota where Id like '%" +txtId.getText()+"%'" +"or Nama like '%" + txtId.getText() + "%'"; 
                Statement stat = conn.createStatement();
                rs = stat.executeQuery(sql);

                cbJenisKelamin.setSelectedItem("Laki-Laki");
                txtId.setText("");
                txtNama.setText("");
                txtAlamat.setText("");
                jdTanggalLahir.setDateFormatString("");
                txtPekerjaan.setText("");
                btnHapus.setEnabled(true);
                btnEdit.setEnabled(true);
                txtId.requestFocus();
                while (rs.next()) {
                    tabelanggota.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
                }
               tblData.setModel(tabelanggota);
               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan ");
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

