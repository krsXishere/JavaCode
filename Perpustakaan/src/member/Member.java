    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String tanggal = String.valueOf(fm.format(jdTanggalLahir.getDate()));
        if((txtId.getText().equals(""))||(txtNama.getText().equals(""))||(txtAlamat.getText().equals(""))||(tanggal.equals(""))||(txtPekerjaan.getText().equals(""))){
            JOptionPane.showMessageDialog(btnSimpan, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
        }else {
         String sqlkode="Insert into anggota(Id,Nama,Alamat,Ttl,Jkl,Pekerjaan) values ('"+this.txtId.getText()+"',"+ "'"+this.txtNama.getText()+"',"+ "'"+this.txtAlamat.getText()+"',"+"'"+tanggal+"',"+"'"+this.cbJenisKelamin.getSelectedItem()+"',"+"'"+this.txtPekerjaan.getText()+"')";
            try { //jalankan query tersebut
               ps = conn.prepareStatement(sqlkode);
               ps.executeUpdate();
               ps.close();
               
               //tampilkan data
                refreshTable();
                JOptionPane.showMessageDialog(this, "Simpan Berhasil","Informasi",1);
                cbJenisKelamin.setSelectedItem("Laki-Laki");
                txtId.setText("");
                txtNama.setText("");
                txtAlamat.setText("");
                jdTanggalLahir.setDateFormatString("");
                txtPekerjaan.setText("");
                btnHapus.setEnabled(true);
                btnEdit.setEnabled(true);
                txtId.requestFocus();
           
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

